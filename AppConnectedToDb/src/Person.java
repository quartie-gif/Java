public class Person {
    String firstName;
    String lastName;
    String fieldOfStudy;
    String sex;
    int year;


    Person(String firstName, String lastName, String fieldOfStudy, int year, String sex) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.fieldOfStudy = fieldOfStudy;
        this.year = year;
        this.sex = sex;
    }


}
